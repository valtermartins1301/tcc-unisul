package br.com.unisul.bean;

public class StatusPedido {
	private Long id;
	private String descricao;
	
	public StatusPedido(){
		
	}
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
	

}
